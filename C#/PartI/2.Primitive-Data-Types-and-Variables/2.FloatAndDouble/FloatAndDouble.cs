using System;

class FloatAndDouble
{
    static void Main()
    {
        double @double1 = 34.567839023; //double (±5.0 × 10−324 to ±1.7 × 10308): 64-bits, precision of 15-16 digits
        float @float1 = 12.345f; //float (±1.5 × 10−45 to ±3.4 × 1038): 32-bits, precision of 7 digits
        double @double2 = 8923.1234857; //double (±5.0 × 10−324 to ±1.7 × 10308): 64-bits, precision of 15-16 digits
        float @float2 = 3456.091f; //float (±1.5 × 10−45 to ±3.4 × 1038): 32-bits, precision of 7 digits
    }
}
