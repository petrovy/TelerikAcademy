using System;

class DeclareFiveVariables
{
    static void Main()
    {
        ushort @ushort = 52130; //ushort (0 to 65,535): unsigned 16-bit
        sbyte @sbyte = -115; //sbyte (-128 to 127): signed 8-bit
        int @int = 4825932; //int (-2,147,483,648 to 2,147,483,647): signed 32-bit
        byte @byte = 97; //byte (0 to 255): unsigned 8-bit
        short @short = -10000; //short (-32,768 to 32,767): signed 16-bit
    }
}